public class Courses {
    public String title = "";
    public int price = 0;
    public int copies = 0;

    public void setTitle(String title) {
        this.title = title;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public void setCopies(int copies) {
        this.copies = copies;
    }


    public String getTitle() {
        return title;
    }

    public int getPrice() {
        return price;
    }

    public int getCopies() {
        return copies;
    }


}
